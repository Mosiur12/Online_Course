        $payments = DB::table('payments')
            ->join('courses', 'payments.course_id', '=', 'courses.id')
            ->join('users', 'users.id', '=', 'courses.instructor_id')
            ->select('payments.*','courses.title', 'users.name')
            ->orderByDesc('id')
            ->get();
        //return $payments;

